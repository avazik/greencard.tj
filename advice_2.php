	<h4 class="mt-4 mb-4" align="center">ПРАВИЛЬНОЕ ЗАПОЛНЕНИЕ АНКЕТЫ </h4>
  <h5>Требования к фото</h5><br>
<p>Перед подачей заявки надо сделать фотографию и получить ее в цифровом виде. Можно отсканировать печатную фотографию, но проще сделать новую. Фотографии должны быть у всех членов семьи, которых вы указываете в анкете.</p>

<p>К фотографиям предъявляются строгие требования. Вот основные:<br><br>

- Фото должно быть сделано не ранее чем за 6 месяцев до подачи заявки.<br>
- Фото должно быть цветным и квадратным. Голова должна быть в центре и занимать 50—69% от высоты изображения.<br>
- Размер печатной фотографии — 51 × 51 мм, цифровой — от 600 × 600 до 1200 × 1200 пикселей. Формат — JPG, максимально допустимый размер — 240 Кб.<br>
- Фото должно быть четким, на однотонном светлом фоне, без теней, с точно переданным тоном кожи.<br>
- Фото нельзя ретушировать, обрабатывать в фоторедакторе или изменять.<br>
- Фотографируйтесь в обычной одежде, без очков и головного убора. Униформа не подойдет, но можно делать фото в одежде, которую вы носите по религиозным соображениям или медицинским предписаниям.<br>
- Уберите волосы с лица, смотрите прямо в камеру с нейтральным выражением лица, чтобы были хорошо видны глаза.<br>
- Если фотографируете ребенка, на снимке не должно быть других людей, ребенок должен смотреть в камеру с нейтральным выражением лица. Допустимо, чтобы глаза новорожденного были полуприкрыты или закрыты.<br><br>

Чтобы сфотографировать младенца, положите его на спину на ткань белого или близкого к белому цвета. Убедитесь, что на лицо ребенка не падает тень, особенно когда вы держите камеру над ним. Также можно накрыть автомобильное кресло тканью белого или близкого к белому цвета и сделать фотографию ребенка в кресле.<br>


